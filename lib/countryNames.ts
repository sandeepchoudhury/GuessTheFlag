/** Resolve a country's display name for a locale, falling back to English. */
export function localizedCountryName(iso: string, englishName: string, locale: Locale): string {
  if (locale === 'en') return englishName;
  return COUNTRY_NAMES[iso]?.[locale] ?? englishName;
}
